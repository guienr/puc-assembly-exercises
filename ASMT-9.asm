TITLE VERIFICA QUANTIDADE DE CARACTER

.MODEL SMALL

.STACK 100

.DATA
STRING DB "ABACAXI$"

.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV ES, AX

    CLD

    LEA DI, STRING

    XOR BX, BX
    MOV CX, 7

    MOV AL, "A"
    
    VERIFICA:
    SCASB
    JE ENCONTRADO
    LOOP VERIFICA
    JMP FINAL
    
    ENCONTRADO:
    INC BX
    LOOP VERIFICA

    FINAL:
    OR BL, 30h
    MOV AH, 02h
    MOV DL, BL
    INT 21h

    MOV AH, 4CH
    INT 21h
MAIN ENDP
END MAIN