TITLE EX6

.MODEL SMALL

.DATA
MSG DB "DIGITE UM NUMERO(0 A 9): $"

.CODE
MAIN PROC

MOV AX, @DATA
MOV DS, AX

MOV AH, 09h
MOV DX, OFFSET MSG
INT 21H

MOV AH, 01h
INT 21H
MOV CL, AL
SUB CX, "0"

PRINT_ASTERISCO:

MOV AH, 02h
MOV DL, 13
INT 21H
MOV DL, 10
INT 21H
MOV DL, "*"
INT 21H

LOOP PRINT_ASTERISCO

MOV AH, 4CH

INT 21h
MAIN ENDP
END MAIN