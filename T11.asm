TITLE AULA11

.MODEL SMALL

.STACK

SALVA MACRO 
    PUSH AX
    PUSH BX
    PUSH CX
    PUSH DX
ENDM

RECUP MACRO
    POP DX
    POP CX
    POP BX
    POP AX
ENDM

PRINT MACRO STR
    MOV AH, 09h
    LEA DX, STR
    INT 21h
ENDM

COPIA MACRO
    LEA SI, STR1
    LEA DI, STR2

    MOV CX, N
    REP MOVSB
ENDM

.DATA
STR1 DB "HELLO WORLD!$"
STR2 DB 13 DUP("?")
N EQU 13

.CODE 
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV ES, AX

    CLD

    PRINT STR1
    COPIA
    PRINT STR2

MOV AH, 4CH
INT 21h
MAIN ENDP
END MAIN